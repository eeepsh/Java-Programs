package javaClassat7;

public class LoopingTest
{
	public static void main (String[] args)
	{
		int a=1;
		while (a<10)
		{
			System.out.println("hi");
			a++;
		}
		int b=1;
		while(b<10)
		{
			System.out.println(b);
			b++;
		}
		int c=10;
		while (c>1)
		{
			System.out.println(c);
			c--;
		}
	}
}
